
// abs -> this method change nagetive sign to positive
console.log(Math.abs(-19));
console.log(Math.abs(12));

//exp
console.log(Math.exp(4)); //54.598150033144236
console.log(Math.exp(-2)); //0.1353352832366127

//trunc

console.log(Math.trunc(4.856));  // 4

//sqrt
console.log(Math.sqrt(25)); // 5
console.log(Math.sqrt(49)); // 7

//round
console.log(Math.round(12.8)); // 13
console.log(Math.round(7.3));  // 7

//ceil -> round down
console.log(Math.ceil(4.3)); //5
console.log(Math.ceil(8.8)); //9

//floor -> round up
console.log(Math.floor(3.4)); //3
console.log(Math.floor(3.8)); //3

//PI
console.log(Math.PI); // 3.141592653589793

//pow
console.log(Math.pow(2, 4));  //16
console.log(Math.pow(3, 3));  //27

// min
console.log(Math.min(12, 11));  // 11
console.log(Math.min(12, 32, 5, 54));  //5

//max
console.log(Math.max(12, 11));  // 12
console.log(Math.max(12, 32, 5, 54));  //54